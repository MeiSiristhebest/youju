export function getPinyinInitials(text: string): string {
  let result = ''
  for (const char of text) {
    const initial = PINYIN_MAP[char]
    if (initial) {
      result += initial.charAt(0).toLowerCase()
    } else if (/[a-zA-Z0-9]/.test(char)) {
      result += char.toLowerCase()
    }
  }
  return result
}

export function matchCommand(query: string, commandName: string): boolean {
  if (!query) return true
  const q = query.toLowerCase()
  const name = commandName.toLowerCase()
  if (name.includes(q)) return true
  const initials = getPinyinInitials(commandName)
  return initials.includes(q)
}
